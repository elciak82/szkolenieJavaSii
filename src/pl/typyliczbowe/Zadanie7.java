package pl.typyliczbowe;

public class Zadanie7 {

    public static void main(String[] args) {
        short first = 2;
        System.out.println("int >>" + first);
        System.out.println("long  >>" + (long) first);
        System.out.println("Float >>" + (float) first);
        System.out.println("double >>" + (double) first);
        long second = 20L;
        System.out.println("int >>" + (int) second);
        System.out.println("byte >>" + (byte) first);
        char third = 65;
        System.out.println("int >>" + third);
    }
}

